 public boolean login() throws SQLException {
    if (cbjabatan.getSelectedItem().equals("pemilik")){
        try{
        Statement stat = (Statement) koneksiKU.getKoneksi().createStatement();
        Data = stat.executeQuery("select id_login, password from login where id_login ='"+user+"'"
        + "and password ='"+pass+"' ");
        berhasil = Data.next();
        
            plogin.hide();
            laporan.show();
            keluar.show();
            Jabatan.setText("PEMILIK");
        
    }catch (SQLException salah){
            System.out.println(salah);
            }
    
   
    }  else if (cbjabatan.getSelectedItem().equals("admin1")){
        try{
        Statement stat = (Statement) koneksiKU.getKoneksi().createStatement();
        Data = stat.executeQuery("select id_login,password from login where id_login ='"+user+"'"
        + "and password = '"+pass+"'");
            plogin.hide();
            data.show();
            pegawai.show();
            pelanggan.show();
            jenis.show();
            masuk.show();
            pembayaran.show();
            keluar.show();
            laporan.show();
            in.show();
            Jabatan.setText("ADMIN");
        
        }catch (SQLException salah){
            System.out.println(salah);
        }
    }
        return berhasil;
    }
 
 
        
