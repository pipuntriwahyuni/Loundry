     public void hapusDataPELANGGAN(){
        //panggil fungsi ambil data
        loadDatapeglanggan(); 
        
        //Beri peringatan sebelum melakukan penghapusan data
        int pesan = JOptionPane.showConfirmDialog(null, "HAPUS DATA"+ idp +"?","KONFIRMASI", JOptionPane.OK_CANCEL_OPTION);
        
        //jika pengguna memilih OK lanjutkan proses hapus data
        if(pesan == JOptionPane.OK_OPTION){
            //uji koneksi
            try{
                //buka koneksi ke database
                Statement stat = (Statement) koneksiKU.getKoneksi().createStatement();
                
                //perintah hapus data
                String sql = "DELETE FROM pelanggan WHERE id_pelanggan='"+ idp +"'";
                PreparedStatement p =(PreparedStatement)koneksiKU.getKoneksi().prepareStatement(sql);
                p.executeUpdate();
                
                //fungsi ambil data
                getPELANGGAN();
                
                //fungsi reset data
                baru();
                JOptionPane.showMessageDialog(null, "DATA BERHASIL DIHAPUS");
            }catch(SQLException err){
                JOptionPane.showMessageDialog(null, err.getMessage());
            }
        }
    }
